// > is greater than
x > 10 //return false

// >= greater than or equal
x >= 5 //return true

// < less than
x < -50 //return false

// <= less than or equal
x <= 100 //return true

// == equal to
x == "5" //return true

// != not equal to
x != "b" //return true

// === equal value and type
x === "5" //return false

// !== not equal value or equal type
x !== "5" //return true

//Equality operators
var x = 99;
x == "99" //true
x === "99" //false

